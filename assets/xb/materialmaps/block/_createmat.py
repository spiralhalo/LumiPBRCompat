path = "assets/xb/materialmaps/block/{name}{shapename}.json"
lamppath = "assets/xb/materialmaps/block/{name}{shapename}{lampname}.json"
i = 0
for blocklist in blocklists:
    for block in blocklist:
        for shape in shapelist:
            f = open(path.format(name=block[0], shapename=shape), "w")
            f.write(regularcontent.format(material=block_contents[i]))
            f.write('\n')
            f.close()
        for lampshape in lampshapelist:
            for lamp in lamplist:
                h = open(lamppath.format(name=block[0], shapename=lampshape, lampname=lamp), "w")
                h.write(lampcontent.format(sprite=block[1], material=block_contents[i]))
                h.write('\n')
                h.close()
    i += 1



